/* String in js */
// => A string is a data type used to store text.
// 1. Single quotes => ' ' 
// 2. Double quotes => " " 
// 3. Backticks quotes => ` `

{
    let str1 = 'Hello';
    let str2 = "Hello";
    let str3 = `Hello`;

    console.log(str1);
    console.log(str2);
    console.log(str3);
}

